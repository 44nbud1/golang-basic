package main

import "fmt"

func main() {
	fmt.Println("aan" == "aan") // perbandingan apakah sama ? , return bool
	fmt.Println("aan" != "aan") // perbandingan apakah beda ?, return bool
	fmt.Println(3 < 5)          // apakah kurang dari ?, return bool
	fmt.Println(3 > 5)          // apakah lebih dari ?, return bool
	fmt.Println(3 <= 5)         // apakah kurang dari atau sama dengan ?, return bool
	fmt.Println(3 >= 5)         // apakah lebih dari atau sama dengan ? , return bool
}
